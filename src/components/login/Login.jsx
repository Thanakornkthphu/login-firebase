  return (
    <Typography
      variant="body2"
      color="text.secondary"
      align="center"
      {...props}
    >
      {"Copyright Â© "}
      <Link color="inherit" href="https://mui.com/">
        Thanakorn
      </Link>{" "}
      {new Date().getFullYear()}
      {"."}
    </Typography>
  );
